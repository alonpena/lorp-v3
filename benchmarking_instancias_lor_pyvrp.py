@dataclass
class Instance:
    """
    Contenedor de la instancia usando diccionarios.
      depots : { id: {"x":int, "y":int, "capacity":int, "fixed_cost":int} }
      clients: { id: {"x":int, "y":int, "demand":int} }
      data   : { "n_clients":int, "n_depots":int, "max_depots_open":int, "n_veh":int,
                 "veh_cap":int, "veh_fixed_cost":int, "cola":int }
    """
    depots: Dict[int, Dict]
    clients: Dict[int, Dict]
    data: Dict

    # -------- Parser principal --------
    @staticmethod
    def from_dat(source: Union[str, Path, Iterable[str]]) -> "Instance": # Puede recibir un string, una ruta de archivo o el archivo como tal
        """
        Estructura instancia por defecto
        N clientes
        N depots
        Maximo N depots a abrir (en la version de costo tienes que fijarlo igual al numero de depots)
        N de vehiculos (en el problema de costo tienes que fijarlo igual al n de clientes)

        coord x coord y depots

        coord x coord y clientes

        capacidad vehicles

        capacidad depots

        demands (solo clientes)

        costo fijo depots

        costo fijo vehicles (en el problema no se considera este costo en la FO)

        n° sin consideración
        """

        # leer líneas de ruta o archivo
        if isinstance(source, (str, Path)): #Distingue si es la ruta o el archivo
            text = Path(source).read_text(encoding="utf-8")
            raw = text.splitlines()
        else:
            raw = list(source)


        # eliminar líneas vacías
        lineas = _dep_vacio(raw)
        idx = 0

        # data inicial
        n_clients       = _to_int(lineas[idx]); idx += 1
        n_depots        = _to_int(lineas[idx]); idx += 1
        max_depots_open = _to_int(lineas[idx]); idx += 1
        n_veh           = _to_int(lineas[idx]); idx += 1

        if not (1 <= max_depots_open <= n_depots):
            raise ValueError("max_depots_open debe estar entre 1 y N depots.")

        # coordenadas
        depot_xy, idx  = _lector_coords(lineas, n_depots,  idx) #codificación tuplas con lector coords, n_depots es cont, inicio es idx
        client_xy, idx = _lector_coords(lineas, n_clients, idx)

        # capacidad de vehículos (única)
        vehicle_cap = _to_int(lineas[idx]); idx += 1
        if vehicle_cap <= 0:
            raise ValueError("La capacidad de vehículo debe ser > 0.")

        # capacidades por depósitos
        depot_caps: List[int] = []
        for _ in range(n_depots):
            depot_caps.append(_to_int(lineas[idx])); idx += 1

        # demandas por cliente
        demands: List[int] = []
        for _ in range(n_clients):
            demands.append(_to_int(lineas[idx])); idx += 1

        # costos fijos depósitos
        depot_fixed_costs: List[int] = []
        for _ in range(n_depots):
            depot_fixed_costs.append(_to_int(lineas[idx])); idx += 1

        # costo fijo de vehículos, aún no usado
        veh_fixed_cost = _to_int(lineas[idx]); idx += 1

        # número final
        cola: Optional[int] = None
        if idx < len(lineas):
            cola = _to_int(lineas[idx]); idx += 1

        # construir diccionarios con id secuenciales desde 1


        # diccionario depots
        depots: Dict[int, Dict] = {}
        for k in range(n_depots):
            x, y = depot_xy[k]       #lee de lista de coordenadas
            depots[k+1] = {
                "x": x, "y": y,
                "cap": depot_caps[k],
                "fixed_cost": depot_fixed_costs[k],
            }

        # diccionario clientes
        clients: Dict[int, Dict] = {}
        for k in range(n_clients):
            x, y = client_xy[k]
            clients[k+1] = {
                "x": x, "y": y,
                "demand": demands[k],
            }

        # diccionario data
        data = {
            "n_clients": n_clients,
            "n_depots": n_depots,
            "max_depots_open": max_depots_open,
            "n_veh": n_veh,
            "veh_cap": vehicle_cap,
            "veh_fixed_cost": veh_fixed_cost,  # no usado en FO
            "cola": cola,
        }

        return Instance(depots=depots, clients=clients, data=data)


    # método print control
    def print_instancia(self) -> None:
      print("------ RESUMEN INSTANCIA ------")
      print(f"Depósitos totales   : {self.data['n_depots']}")
      print(f"Clientes totales    : {self.data['n_clients']}")
      print(f"Vehículos disponibles: {self.data['n_veh']}")
      print(f"Capacidad vehículos : {self.data['veh_cap']}")
      print(f"Max depots open     : {self.data['max_depots_open']}")

      if "R" in self.data:
          print(f"R                  : {self.data['R']}")
          print(f"F_R                : {self.data['F_R']}")
          print(f"F_A                : {self.data['F_A']}")
          print(f"Length             : {self.data['Length']}")

      if self.data.get("cola") is not None:
          print(f"(cola: {self.data['cola']})")

      print("-------------------------------")



    # Método print preview depósitos y clientes
    def preview(self, k_depots: int = None, k_clients: int = 5) -> None:
      print("------ DEPÓSITOS ------")

      depot_ids = sorted(self.depots.keys())

      if k_depots is not None:
          depot_ids = depot_ids[:k_depots]

      for i in depot_ids:
          d = self.depots[i]
          print(f"ID {i:<3} | x={d['x']}, y={d['y']} | cap={d['cap']}") #se sacaron costos fijos porque vienen en excel

      print("------------------------")

      print("------ CLIENTES ------")

      client_ids = sorted(self.clients.keys())[:k_clients]

      for j in client_ids:
          c = self.clients[j]
          print(f"ID {j:<3} | x={c['x']}, y={c['y']} | demand={c['demand']}")

      print("----------------------")

