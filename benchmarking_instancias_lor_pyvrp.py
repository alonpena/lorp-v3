"""# Extracción de resultados y cálculo de costos"""

def build_full_report(inst, res, config, info):
    """
    Construye una fila de resultados que combina:
      - Solución heurística PyVRP
      - Benchmark MILP (desde ExcelConfig)
      - Parámetros del modelo e instancia

    ─── FUENTES DE INFORMACIÓN ───────────────────────────────────────────────────
      inst    : instancia adaptada (depósitos activos, capacidades del Excel)
      res     : resultado PyVRP (res.best contiene la mejor solución)
      config  : ExcelConfig con parámetros y resultados MILP
      info    : dict generado por build_full_model (escala, tipos_veh, etc.)

    ─── RECUPERACIÓN DE COSTOS ───────────────────────────────────────────────────
      Las distancias almacenadas en PyVRP ya llevan el factor:
          dist_pyvrp = d_euclid * escala * F_R  (routing)
          dist_pyvrp = d_euclid * escala * F_A  (DA)
      Para obtener el costo real en unidades del problema:
          costo_routing = dist_routing_pyvrp / escala   (ya incluye F_R)
          costo_da      = dist_da_pyvrp      / escala   (ya incluye F_A)

    ─── COSTO TOTAL PyVRP ────────────────────────────────────────────────────────
      total = costo_routing + costo_da + costo_vehiculos + costo_depositos
      donde:
        costo_vehiculos = n_veh_routing_usados * veh_fixed_cost
        costo_depositos = config.cost_depots  (tomado del Excel, fijo por MILP)

    ─── CAPACIDAD INDUCIDA ───────────────────────────────────────────────────────
      cap_inducida_i = Q * floor(Cap_i / Q)   → capacidad efectiva DA (discretizada)
      brecha_cap_i   = cap_inducida_i - Cap_i  ≤ 0  (pérdida por redondeo)

    ─── CHECK DE FACTIBILIDAD ────────────────────────────────────────────────────
      Se verifica ex post que routing + DA ≤ Cap_i por depósito.
      Con la implementación actual (vehículos routing usan capacidad residual),
      la violación no debería ocurrir, pero el check queda como salvaguarda.

    ─── GAP ──────────────────────────────────────────────────────────────────────
      gap = (total_pyvrp - UB_MILP) / UB_MILP
      Positivo: PyVRP es peor que el MILP (esperado en heurística).
      Negativo: PyVRP mejor que el MILP → señal de inconsistencia de modelamiento.
    """

    sol          = res.best
    tipos_veh    = info["tipos_veh"]
    escala       = info["escala"]
    da_asignados = info["da_asignados"]

    ids_deposito = sorted(inst.depots.keys())

    # ─── 1. Inicialización de acumuladores por depósito ──────────────────────────
    por_deposito = {
        d: {
            "dist_routing":    0.0,
            "dist_da":         0.0,
            "n_veh_routing":   0,
            "demanda":         0.0,
            "demanda_da":      0.0,
            "demanda_routing": 0.0,
        }
        for d in ids_deposito
    }

    dist_routing_total     = 0.0
    dist_da_total          = 0.0
    demanda_total_atendida = 0.0
    demanda_total          = sum(c["demand"] for c in inst.clients.values())

    # ─── 2. Recorrer rutas de la solución ────────────────────────────────────────
    for ruta in sol.routes():
        idx_tipo  = ruta.vehicle_type()
        info_tipo = tipos_veh[idx_tipo]

        tipo     = info_tipo["tipo"]      # "routing" o "direct_allocation"
        deposito = info_tipo["deposito"]  # ID real del depósito

        dist = ruta.distance()

        if tipo == "routing":
            por_deposito[deposito]["dist_routing"]  += dist
            por_deposito[deposito]["n_veh_routing"] += 1
            dist_routing_total += dist
        else:
            por_deposito[deposito]["dist_da"] += dist
            dist_da_total += dist

        # Acumular demanda atendida por tipo
        for trip in ruta.trips():
            entrega = trip.delivery()
            if len(entrega) > 0:
                d = entrega[0]
                demanda_total_atendida += d
                por_deposito[deposito]["demanda"] += d
                if tipo == "routing":
                    por_deposito[deposito]["demanda_routing"] += d
                else:
                    por_deposito[deposito]["demanda_da"] += d

    # ─── 3. Cálculo de costos ────────────────────────────────────────────────────
    # Las distancias PyVRP ya llevan F_R / F_A aplicados.
    # Dividir por escala recupera el costo real en unidades del problema.
    costo_routing   = dist_routing_total / escala
    costo_da        = dist_da_total      / escala

    n_veh_total     = sum(por_deposito[d]["n_veh_routing"] for d in por_deposito)
    costo_vehiculos = n_veh_total * inst.data["veh_fixed_cost"]

    # El costo de depósitos viene del MILP (depósitos fijos, decididos previamente)
    costo_depositos   = float(config.cost_depots)
    costo_total_pyvrp = costo_routing + costo_da + costo_vehiculos + costo_depositos

    # ─── 4. Debug global ─────────────────────────────────────────────────────────
    print("\n========== REPORTE GLOBAL ==========")
    print(f"  Distancia routing (PyVRP) : {dist_routing_total:.2f}  →  costo: {costo_routing:.4f}")
    print(f"  Distancia DA (PyVRP)      : {dist_da_total:.2f}  →  costo: {costo_da:.4f}")
    print(f"  Vehículos routing usados  : {n_veh_total}  →  costo: {costo_vehiculos:.4f}")
    print(f"  Costo depósitos (MILP)    : {costo_depositos:.4f}")
    print(f"  COSTO TOTAL PyVRP         : {costo_total_pyvrp:.4f}")
    print(f"  Demanda total             : {demanda_total}")
    print(f"  Demanda atendida          : {demanda_total_atendida}")
    print(f"  Nivel de servicio         : {demanda_total_atendida / demanda_total:.2%}")

    # ─── 5. Check de factibilidad de capacidad por depósito ──────────────────────
    violacion_cap = False
    for i in ids_deposito:
        cap_i         = inst.depots[i]["cap"]
        demanda_dep_i = por_deposito[i]["demanda"]
        exceso_i      = max(0.0, demanda_dep_i - cap_i)
        if exceso_i > 0:
            violacion_cap = True
            print(f"  ⚠ VIOLACIÓN CAPACIDAD Depósito {i}: "
                  f"demanda={demanda_dep_i:.0f} > cap={cap_i:.0f} "
                  f"(exceso={exceso_i:.0f})")

    # ─── 6. Construcción de la fila de resultados ────────────────────────────────
    fila = {}

    # Identificación
    fila["id"]       = config.row_id
    fila["instancia"] = config.instance

    # Parámetros
    fila["F_R"]    = config.F_R
    fila["F_A"]    = config.F_A
    fila["R"]      = config.R
    fila["Length"] = config.Length

    # Costos PyVRP
    fila["costo_routing_pyvrp"]   = costo_routing
    fila["costo_da_pyvrp"]        = costo_da
    fila["costo_vehiculos_pyvrp"] = costo_vehiculos
    fila["costo_depositos"]       = costo_depositos
    fila["costo_total_pyvrp"]     = costo_total_pyvrp

    # Costos MILP (benchmark)
    fila["costo_routing_milp"]   = config.routing_cost_milp
    fila["costo_da_milp"]        = config.da_cost_milp
    fila["costo_vehiculos_milp"] = config.vehicle_cost_milp

    # Demanda
    fila["demanda_total"]    = demanda_total
    fila["demanda_atendida"] = demanda_total_atendida
    fila["nivel_servicio"]   = demanda_total_atendida / demanda_total

    # Factibilidad
    fila["violacion_capacidad"] = violacion_cap

    # ─── 7. Campos por depósito ──────────────────────────────────────────────────
    cap_veh = int(inst.data["veh_cap"])

    for d_id in ids_deposito:
        cap_i = inst.depots[d_id]["cap"]

        # Capacidad inducida: lo que efectivamente puede servir el vehículo DA
        # dada la estructura discreta de trips de capacidad Q.
        # Siempre ≤ cap_i (hay "pérdida" por redondeo si cap_i mod Q ≠ 0).
        n_trips_da_i   = math.floor(cap_i / cap_veh)
        cap_inducida_i = n_trips_da_i * cap_veh
        brecha_cap_i   = cap_inducida_i - cap_i   # ≤ 0

        dem_py = por_deposito[d_id]["demanda"]
        dem_da = por_deposito[d_id]["demanda_da"]
        dem_r  = por_deposito[d_id]["demanda_routing"]
        veh_py = por_deposito[d_id]["n_veh_routing"]
        uso_py = dem_py / cap_i if cap_i > 0 else 0.0

        # Datos MILP del depósito para comparación
        milp_dep = config.depots_milp.get(d_id, {})

        fila[f"d{d_id}_capacidad"]        = cap_i
        fila[f"d{d_id}_cap_inducida"]     = cap_inducida_i
        fila[f"d{d_id}_brecha_cap"]       = brecha_cap_i

        fila[f"d{d_id}_demanda_pyvrp"]    = dem_py
        fila[f"d{d_id}_demanda_da_pyvrp"] = dem_da
        fila[f"d{d_id}_demanda_rt_pyvrp"] = dem_r
        fila[f"d{d_id}_uso_pyvrp"]        = uso_py
        fila[f"d{d_id}_veh_pyvrp"]        = veh_py

        fila[f"d{d_id}_demanda_milp"]     = milp_dep.get("demand",   None)
        fila[f"d{d_id}_uso_milp"]         = milp_dep.get("usage",    None)
        fila[f"d{d_id}_veh_milp"]         = milp_dep.get("vehicles", None)

    # ─── 8. GAP final ────────────────────────────────────────────────────────────
    ub_milp   = config.UB
    gap_final = (costo_total_pyvrp - ub_milp) / ub_milp if ub_milp > 0 else None

    fila["ub_milp"]   = ub_milp
    fila["ub_pyvrp"]  = costo_total_pyvrp
    fila["gap_final"] = gap_final

    print("\n========== GAP ==========")
    print(f"  UB MILP : {ub_milp:.4f}")
    print(f"  PyVRP   : {costo_total_pyvrp:.4f}")
    print(f"  GAP     : {gap_final:.4%}" if gap_final is not None else "  GAP: N/A")
    if gap_final is not None and gap_final < 0:
        print("  ⚠ ADVERTENCIA: PyVRP obtuvo mejor costo que el MILP → revisar modelamiento.")

    return fila


