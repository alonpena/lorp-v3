def solve_fast(m, runtime=5, n_runs=3):
    from pyvrp.stop import MaxRuntime

    best_res = None
    best_cost = float("inf")

    for seed in range(n_runs):
        res = m.solve(
            stop=MaxRuntime(runtime),
            seed=seed,
            display=False
        )

        cost = res.best.distance()

        if cost < best_cost:
            best_cost = cost
            best_res = res

    return best_res

