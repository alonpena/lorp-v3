from collections import defaultdict

def extract_kpis_level1(inst, res, info):
    sol = res.best
    veh_type_kind = info["veh_type_kind"]

    # -------------------------
    # KPIs globales por tipo
    # -------------------------
    per_type = defaultdict(lambda: {
        "n_vehicles": 0,   # rutas
        "distance": 0.0,
        "n_clients": 0,
        "demand": 0.0,
    })

    # -------------------------
    # Recorrer solución
    # -------------------------
    for route in sol.routes():

        # Tipo de vehículo
        vt_idx = route.vehicle_type()
        vt_info = veh_type_kind[vt_idx]
        kind = vt_info["type"]
        depot = vt_info["depot"]

        # 1) Cada ruta = 1 vehículo
        per_type[kind]["n_vehicles"] += 1

        # 2) Distancia
        route_dist = route.distance()
        per_type[kind]["distance"] += route_dist

        # 3) Trips → clientes + demanda
        for trip in route.trips():
            visits = trip.visits()
            per_type[kind]["n_clients"] += len(visits)

            delivery = trip.delivery()
            if len(delivery) > 0:
                per_type[kind]["demand"] += delivery[0]

    # -------------------------
    # Totales globales
    # -------------------------
    total_distance = sol.distance()
    total_demand = sum(c["demand"] for c in inst.clients.values())

    # Parámetros iniciales y brecha
    served_demand = sum(vals["demand"] for vals in per_type.values())
    pending_demand = total_demand - served_demand

    return {
        "total_distance": total_distance,
        "total_demand": total_demand,
        "served_demand": served_demand,
        "pending_demand": pending_demand,
        "per_type": dict(per_type),
    }


