import math
from typing import Dict, List, Tuple

#Distancia euclídea entre (x,y). Devuelve float.
def _dist_euclid(a, b):
    return math.hypot(a[0] - b[0], a[1] - b[1])


#Distancia Manhattan entre (x,y). Devuelve float.
def _dist_manhattan(a, b):
    return abs(a[0] - b[0]) + abs(a[1] - b[1])


def build_direct_allocation_data(inst: Instance, radius: float) -> tuple[dict, int]:

    da_data = {}
    max_clients_per_depot = 0


    for i, depot in inst.depots.items():
        xi, yi = depot["x"], depot["y"]

        clients_i = []
        arcs_ij = []
        arcs_ji = []
        cost_ij = {}
        cost_ji = {}

        for j, client in inst.clients.items():
            xj, yj = client["x"], client["y"]
            dij = _dist_euclid((xi, yi), (xj, yj))

            if dij <= radius:
                clients_i.append(j)

                arcs_ij.append((i, j))
                arcs_ji.append((j, i))

                cost_ij[(i, j)] = dij
                cost_ji[(j, i)] = 0.0

        if clients_i:
            da_data[i] = {
                "clients": clients_i,
                "arcs_ij": arcs_ij,
                "arcs_ji": arcs_ji,
                "cost_ij": cost_ij,
                "cost_ji": cost_ji,
            }

            # actualizar máximo clientes global
            if len(clients_i) > max_clients_per_depot:
                max_clients_per_depot = len(clients_i)

    return da_data, max_clients_per_depot

    """
    Construye la estructura de direct allocation por depósito.

    Retorna:
      - da_data: dict indexado por depósito con:
          * clients: clientes factibles
          * arcs_ij: arcos depot -> cliente
          * arcs_ji: arcos cliente -> depot
          * cost_ij: costo depot -> cliente
          * cost_ji: costo cliente -> depot (= 0)
      - max_clients_per_depot: máximo número de clientes factibles entre depósitos
    """

"""# Función para escalar con Arslam


Escalamiento de distancias (tipo Arslan)

Se calcula la distancia máxima global entre todos los nodos (depósitos y clientes) mediante la función compute_max_distance. Con este valor se define un factor de escalamiento:

scale = 100 / maxdist

Luego, todas las distancias que se agregan al modelo (arcos de routing y de direct allocation) se multiplican por este factor justo antes de usar m.add_edge().


*  Las distancias originales NO se modifican.
* El filtrado de clientes por radio (direct allocation) se hace con distancias reales.


"""

def compute_max_distance(inst):
    """
    Replica EXACTAMENTE el comportamiento del C++:
    maxdist sobre TODOS los nodos (depots + clientes)
    """

    # unir todos los nodos (como en C++)
    nodes = []

    for d in inst.depots.values():
        nodes.append((d["x"], d["y"]))

    for c in inst.clients.values():
        nodes.append((c["x"], c["y"]))

    maxdist = 0.0

    for i in range(len(nodes)):
        for j in range(len(nodes)):
            dx = nodes[i][0] - nodes[j][0]
            dy = nodes[i][1] - nodes[j][1]
            dist = math.hypot(dx, dy)

            if dist > maxdist:
                maxdist = dist

    return maxdist

"""# Política de asignación directa"""

def assign_da_clients(inst: Instance, da_data: dict, veh_cap: int) -> tuple[dict, set]:
    """
    Heuristic DA client assignment.

    Policy:
    - Each eligible client is assigned to its nearest feasible depot.
    - DA capacity per depot = veh_cap * floor(cap_depot / veh_cap)
      (induced by multitrip vehicle structure).
    - If nearest depot is full: try next nearest; if all full → routing.
    - Clients with no feasible depot (outside all radii) → routing.

    Supuesto explícito: se privilegia DA sobre routing cuando el cliente
    es factible. La prioridad entre clientes del mismo depósito es por
    menor distancia al depósito (nearest-first).

    Returns:
      da_assigned : {depot_id: [client_ids]}
      routing_set : set of client_ids that go to routing
    """
    veh_cap = int(veh_cap)

    # DA capacity and usage per depot
    da_cap  = {}
    da_used = {}
    for i, d in inst.depots.items():
        n_trips   = math.floor(d["cap"] / veh_cap)
        da_cap[i] = n_trips * veh_cap   # Q * floor(Cap_i / Q)
        da_used[i] = 0.0

    da_assigned = {i: [] for i in inst.depots}
    routing_set = set()

    for j, client in inst.clients.items():
        dj = client["demand"]

        # Feasible depots for this client (within radius)
        feasible = []
        for i, perfil in da_data.items():
            if j in perfil["clients"]:
                dist_ij = perfil["cost_ij"][(i, j)]
                feasible.append((dist_ij, i))

        if not feasible:
            routing_set.add(j)
            continue

        # Try nearest feasible depot first
        feasible.sort()   # ascending distance

        assigned = False
        for dist_ij, i in feasible:
            if da_used[i] + dj <= da_cap[i]:
                da_assigned[i].append(j)
                da_used[i] += dj
                assigned = True
                break

        if not assigned:
            # All feasible depots are full
            routing_set.add(j)

    # Remove depots with no assigned clients
    da_assigned = {i: clients for i, clients in da_assigned.items() if clients}

    return da_assigned, routing_set

"""# Codificación Clases a PyVRP"""

from pyvrp import Model
import math

def build_full_model(inst: Instance) -> tuple[Model, dict]:
    """
    Construye el modelo PyVRP para el LoRP-FSD con dos modos de servicio:

      - prof_routing       : rutas vehiculares clásicas (CVRP multi-depósito)
      - prof_direct_alloc  : asignación directa (DA), un vehículo multitrip por depósito

    ─── DECISIONES DE MODELAMIENTO ──────────────────────────────────────────────────

    1. PREPROCESO DA (heurístico):
       Se llama a assign_da_clients() antes de construir el modelo.
       Los clientes factibles (dentro del radio R de algún depósito) se asignan
       prioritariamente por DA al depósito más cercano, respetando la capacidad
       inducida:  Cap^DA_i = Q * floor(Cap_i / Q)
       Los clientes no asignados por DA pasan a routing.

    2. SEPARACIÓN DE GRAFOS:
       - Clientes DA solo tienen arcos en prof_direct_alloc → no pueden ser
         visitados por vehículos de routing.
       - Clientes routing solo tienen arcos en prof_routing → no pueden ser
         atendidos por DA.
       Esto garantiza que la separación DA/routing del preproceso se respete
       dentro del solver.

    3. VEHÍCULOS ROUTING (por depósito i):
       La capacidad disponible para routing es la capacidad residual tras
       descontar la demanda asignada a DA:
         Cap^routing_i = Cap_i - demanda_DA_i
         N_veh_routing_i = floor(Cap^routing_i / Q)
       Supuesto: se respeta la capacidad del depósito de forma estructural.

    4. VEHÍCULO DA MULTITRIP (por depósito i):
       Un único vehículo DA que puede recargar en su propio depósito:
         num_available = 1
         capacity      = Q  (carga por trip)
         max_reloads   = floor(Cap_i / Q) - 1
       Capacidad efectiva DA = Q * floor(Cap_i / Q).
       Justificación de max_reloads: un vehículo ya ejecuta 1 trip sin recargar;
       k trips totales requieren k-1 reloads intermedios.

    5. ESCALAMIENTO DE DISTANCIAS (tipo Arslan):
       scale = 100 / max_dist_global   (sobre todos los nodos de inst)
       Las distancias almacenadas en PyVRP son: d_euclid * scale * F_R  (routing)
                                                d_euclid * scale * F_A  (DA)
       Para recuperar costos reales en el reporte: dist_pyvrp / scale

    6. COSTO FIJO DE DEPÓSITO:
       No se agrega al modelo PyVRP. Se toma directamente del Excel (config.cost_depots)
       y se suma manualmente en build_full_report. Los depósitos abiertos ya están
       fijados por la solución MILP.
    """

    # ─── Validaciones básicas ────────────────────────────────────────────────────
    if not inst.depots:
        raise ValueError("La instancia no tiene depósitos.")
    if not inst.clients:
        raise ValueError("La instancia no tiene clientes.")

    # ─── Parámetros globales del modelo ──────────────────────────────────────────
    radio        = float(inst.data["R"])           # radio de cobertura DA
    largo_ruta   = float(inst.data["Length"])       # distancia máxima por ruta
    cap_veh      = int(inst.data["veh_cap"])        # capacidad del vehículo
    F_R          = float(inst.data["F_R"])          # ponderador costo routing
    F_A          = float(inst.data["F_A"])          # ponderador costo DA

    # ─── 1. Factibilidad DA (qué clientes están dentro del radio) ────────────────
    da_data, _ = build_direct_allocation_data(inst, radio)

    # ─── 2. Preproceso: asignación heurística DA ─────────────────────────────────
    # Se privilegia DA sobre routing cuando el cliente es factible.
    # Política: asignar al depósito más cercano, respetando Cap^DA_i = Q * floor(Cap_i/Q).
    # Si el depósito más cercano está lleno, se intenta el siguiente más cercano.
    # Si ningún depósito tiene capacidad disponible → pasa a routing.
    # Clientes fuera de todo radio → routing directamente.
    da_asignados, clientes_routing = assign_da_clients(inst, da_data, cap_veh)

    # ─── 3. Modelo PyVRP ─────────────────────────────────────────────────────────
    m = Model()
    prof_routing      = m.add_profile(name="routing")
    prof_direct_alloc = m.add_profile(name="direct_allocation")

    # ─── 4. Nodos: depósitos y clientes ──────────────────────────────────────────
    nodos_deposito = {
        i: m.add_depot(x=d["x"], y=d["y"])
        for i, d in inst.depots.items()
    }

    nodos_cliente = {
        j: m.add_client(x=c["x"], y=c["y"], delivery=int(c["demand"]))
        for j, c in inst.clients.items()
    }

    # ─── 5. Factor de escalamiento (todos los nodos de inst) ─────────────────────
    # IMPORTANTE: se pasa inst (instancia modificada) no una variable global.
    max_dist = compute_max_distance(inst)
    escala   = 100.0 / max_dist

    # ─── 6. Arcos de ROUTING (grafo completo sobre depósitos + clientes routing) ──
    # Los clientes DA NO tienen arcos en prof_routing → el solver no puede
    # visitarlos con vehículos de routing.
    nodos_routing = {}
    for i, nodo in nodos_deposito.items():
        nodos_routing[f"d{i}"] = nodo
    for j in clientes_routing:
        nodos_routing[f"c{j}"] = nodos_cliente[j]

    items_routing = list(nodos_routing.items())
    for id_i, nodo_i in items_routing:
        for id_j, nodo_j in items_routing:
            if id_i == id_j:
                continue
            dist = _dist_euclid((nodo_i.x, nodo_i.y), (nodo_j.x, nodo_j.y))
            # Distancia almacenada en PyVRP: d_euclid * escala * F_R
            # Para recuperar costo real: dist_pyvrp / escala  (ya lleva F_R)
            m.add_edge(nodo_i, nodo_j,
                       distance=dist * escala * F_R,
                       profile=prof_routing)

    # ─── 7. Arcos de DA (solo pares depósito i → cliente j asignados) ────────────
    # Arco de ida:   cost = d_euclid(i,j) * escala * F_A
    # Arco de vuelta: cost = 0  (el retorno al depósito no tiene costo en DA)
    for i, lista_clientes in da_asignados.items():
        nodo_dep = nodos_deposito[i]
        xi, yi   = inst.depots[i]["x"], inst.depots[i]["y"]

        for j in lista_clientes:
            nodo_cli = nodos_cliente[j]
            xj, yj   = inst.clients[j]["x"], inst.clients[j]["y"]

            dist_ida    = _dist_euclid((xi, yi), (xj, yj))
            dist_vuelta = 0.0  # supuesto: costo de retorno al depósito = 0

            m.add_edge(nodo_dep, nodo_cli,
                       distance=dist_ida * escala * F_A,
                       profile=prof_direct_alloc)
            m.add_edge(nodo_cli, nodo_dep,
                       distance=dist_vuelta,
                       profile=prof_direct_alloc)

    # ─── 8. Tipos de vehículo ─────────────────────────────────────────────────────
    tipos_veh = []   # lista paralela al orden interno de PyVRP

    # ── 8a. Vehículos de ROUTING por depósito ────────────────────────────────────
    # Capacidad disponible para routing = Cap_i − demanda_DA_i
    # N_veh_routing_i = floor(Cap^routing_i / Q)
    # Esto garantiza estructuralmente que routing + DA ≤ Cap_i.
    for i, d in inst.depots.items():
        cap_dep      = d["cap"]
        demanda_da_i = sum(inst.clients[j]["demand"] for j in da_asignados.get(i, []))
        cap_routing  = cap_dep - demanda_da_i        # capacidad residual para routing
        n_veh        = math.floor(cap_routing / cap_veh)

        if n_veh <= 0:
            continue

        m.add_vehicle_type(
            num_available = n_veh,
            capacity      = cap_veh,
            max_distance  = largo_ruta * escala,  # largo_ruta escalado
            start_depot   = nodos_deposito[i],
            end_depot     = nodos_deposito[i],
            profile       = prof_routing,
        )
        tipos_veh.append({
            "tipo":           "routing",
            "deposito":       i,
            "num_disponible": n_veh,
        })

    # Después del bloque de vehículos routing por depósito
        residuo = int(cap_routing % cap_veh)
        if residuo > 0:
            m.add_vehicle_type(
                num_available = 1,
                capacity      = residuo,          # capacidad exacta del sobrante
                max_distance  = largo_ruta * escala,
                start_depot   = nodos_deposito[i],
                end_depot     = nodos_deposito[i],
                profile       = prof_routing,
            )
            tipos_veh.append({
                "tipo":           "routing",
                "deposito":       i,
                "num_disponible": 1,
            })

    # ── 8b. Vehículo DA MULTITRIP por depósito ───────────────────────────────────
    # Un único vehículo DA con recargas en el mismo depósito de origen.
    #
    # Parámetros:
    #   capacity    = Q      (carga por trip)
    #   max_reloads = k - 1  donde k = floor(Cap_i / Q)
    #
    # Interpretación: k trips totales requieren k-1 recargas intermedias.
    # El solver decide cuántos clientes DA asigna al vehículo dentro del límite.
    for i, lista_clientes in da_asignados.items():
        cap_dep  = inst.depots[i]["cap"]
        n_trips  = math.floor(cap_dep / cap_veh)

        if n_trips <= 0:
            continue

        m.add_vehicle_type(
            num_available = 1,
            capacity      = cap_veh,
            start_depot   = nodos_deposito[i],
            end_depot     = nodos_deposito[i],
            reload_depots = [nodos_deposito[i]],   # recarga en el mismo depósito
            max_reloads   = n_trips - 1,
            profile       = prof_direct_alloc,
        )
        tipos_veh.append({
            "tipo":           "direct_allocation",
            "deposito":       i,
            "num_disponible": 1,
        })

    # ─── 9. Información auxiliar para KPIs y reporte ─────────────────────────────
    info = {
        "da_data":          da_data,          # clientes factibles DA por depósito
        "da_asignados":     da_asignados,     # clientes efectivamente asignados a DA
        "clientes_routing": clientes_routing, # clientes que van por routing
        "escala":           escala,           # factor de escalamiento
        "tipos_veh":        tipos_veh,        # lista paralela de tipos de vehículo
        "nodos_deposito":   nodos_deposito,   # nodos PyVRP de depósitos
        "nodos_cliente":    nodos_cliente,    # nodos PyVRP de clientes
    }

    return m, info

