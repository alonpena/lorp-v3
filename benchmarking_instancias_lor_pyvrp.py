# Depurador espacios vacíos -> Retorna lista de líneas sin espacios vacíos
def _dep_vacio(lines: Iterable[str] ) -> List[str]:
    txt = []
    for l in lines:
        s = l.strip()          # Quita los espacios de los bordes de la línea (no afecta coordenadas)
        if s != "":             # Evalúa línea tiene contenido, si es así agrega e itera.
            txt.append(s)
    return txt

# Parser str -> int
def _to_int(s: str) -> int:
    return int(s)

# Lector coordenadas

def _lector_coords(block: List[str], cont: int, inicio: int) -> Tuple[List[Tuple[int, int]], int]:

    """
    Lee cont (i.e. n_clientes, n_depots, etc) líneas consecutivas desde `block` a partir del índice `inicio`,
    interpretando cada línea como un par de coordenadas (x, y).

    Parámetros
    ----------
    block : List[str] # lista de líneas del archivo sin líneas vacías.
    cont : int # número de coordenadas a leer.
    inicio : int # índice inicial dentro de `block`.

    Returns
    -------
    coords : List[Tuple[int, int]] #  Lista de coordenadas (x, y) en formato entero.
    i : int # índice de `block` inmediatamente posterior a la última línea leída.
    """
    coords: List[Tuple[int, int]] = []      # setteo lista para tuplas
    i = inicio

    for k in range(cont):                   # itera en todas las líneas, verifica que sean dos coordenadas y la agrega a la lista
        linea = block[i].split()             #.split() devuelve una lista con los elementos separados; i.e. "100 200" -> ['100', '200']

        if len(linea) != 2:                 # verificación de dos coordenadas
            raise ValueError(f"Línea {i+1} debería tener dos números 'x y'. Encontrado: '{linea}'")

        x, y = int(linea[0]), int(linea[1])
        coords.append((x, y))
        i += 1

    return coords, i


