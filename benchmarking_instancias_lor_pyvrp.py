def build_full_report(inst, res, config, info):
    """
    Construye una fila de resultados combinando:
    - Solución heurística (PyVRP)
    - Benchmark MILP (Excel)
    - Parámetros del modelo

    -------------------------
    FUENTES DE INFORMACIÓN
    -------------------------
    - inst: instancia adaptada (depósitos y parámetros modificados)
    - res.best: solución PyVRP
    - config: parámetros y resultados MILP (Excel)
    - info: metadata de tipos de vehículo

    -------------------------
    MÉTODOS PYVRP USADOS
    -------------------------
    - sol.routes()
    - route.distance()
    - route.trips()
    - trip.delivery()
    - route.vehicle_type()
    - route.end_depot()

    -------------------------
    DECISIONES DE MODELADO
    -------------------------
    - Depósitos fijos (definidos por MILP)
    - Costos de depósito vienen del Excel (no PyVRP)
    - Clasificación:
        * routing → rutas normales
        * direct_allocation → cliente directo
    """
    sol = res.best
    veh_type_kind = info["veh_type_kind"]

    real_depot_ids = sorted(inst.depots.keys())

    # =========================
    # 1) INICIALIZACIÓN
    # =========================
    per_depot = {
        d: {
            "routing_dist": 0.0,
            "da_dist": 0.0,
            "n_veh_routing": 0,
            "demand": 0.0,
        }
        for d in real_depot_ids
    }

    total_routing_dist = 0.0
    total_da_dist = 0.0
    total_demand_served = 0.0

    total_demand = sum(c["demand"] for c in inst.clients.values())

    # =========================
    # 2) RECORRER SOLUCIÓN
    # =========================
    for route in sol.routes():
        vt_idx = route.vehicle_type()
        raw_kind = veh_type_kind[vt_idx]

        # Clasificación de tipo de ruta
        kind = "da" if raw_kind.startswith("direct_allocation") else "routing"

        dist = route.distance()

        # Mapear índice interno de PyVRP → ID real de depósito
        depot_internal = route.end_depot()
        depot = real_depot_ids[depot_internal]

        if kind == "routing":
            per_depot[depot]["routing_dist"] += dist
            per_depot[depot]["n_veh_routing"] += 1
            total_routing_dist += dist
        else:
            per_depot[depot]["da_dist"] += dist
            total_da_dist += dist

        # Demanda atendida
        for trip in route.trips():
            delivery = trip.delivery()
            if len(delivery) > 0:
                d = delivery[0]  # asumimos demanda escalar
                total_demand_served += d
                per_depot[depot]["demand"] += d

    # =========================
    # 3) COSTOS PYVRP
    # =========================
    routing_cost = total_routing_dist * config.F_R
    da_cost = total_da_dist * config.F_A

    n_veh_total = sum(per_depot[d]["n_veh_routing"] for d in per_depot)
    vehicle_cost = n_veh_total * inst.data["veh_fixed_cost"]

    # costo de depósitos viene del MILP (Excel)
    depot_cost = config.cost_depots

    total_cost_pyvrp = routing_cost + da_cost + vehicle_cost + depot_cost

    # =========================
    # 4) DEBUG GLOBAL
    # =========================
    print("\n========== GLOBAL ==========")
    print("Routing dist:", total_routing_dist)
    print("DA dist:", total_da_dist)
    print("Routing cost:", routing_cost)
    print("DA cost:", da_cost)
    print("Vehicles routing:", n_veh_total)
    print("Vehicle cost:", vehicle_cost)
    print("Depot cost:", depot_cost)
    print("TOTAL COST:", total_cost_pyvrp)
    print("Demand served:", total_demand_served)
    print("Total demand:", total_demand)
    print("Service %:", total_demand_served / total_demand)

    # =========================
    # 5) REPORTE FINAL
    # =========================
    row = {}

    # -------- IDENTIFICACIÓN --------
    row["id"] = config.row_id
    row["instance"] = config.instance

    # -------- PARÁMETROS --------
    row["F_R"] = config.F_R
    row["F_A"] = config.F_A

    # -------- COSTOS FIJOS --------
    row["depot_cost"] = depot_cost
    row["vehicle_cost_unit"] = inst.data["veh_fixed_cost"]

    # -------- MILP --------
    row["routing_cost_milp"] = getattr(config, "routing_cost_milp", None)
    row["da_cost_milp"] = getattr(config, "da_cost_milp", None)
    row["vehicle_cost_milp"] = getattr(config, "vehicle_cost_milp", None)

    # -------- PYVRP --------
    row["routing_cost_pyvrp"] = routing_cost
    row["da_cost_pyvrp"] = da_cost
    row["vehicle_cost_pyvrp"] = vehicle_cost
    row["total_cost_pyvrp"] = total_cost_pyvrp

    # -------- DEMANDA --------
    row["total_demand"] = total_demand
    row["served_demand"] = total_demand_served
    row["service_level"] = total_demand_served / total_demand

    # -------- DEPOTS --------
    for d_id, d_info in config.depots.items():
        cap = d_info["capacity"]

        d_py = per_depot[d_id]["demand"]
        veh_py = per_depot[d_id]["n_veh_routing"]

        usage_py = d_py / cap if cap > 0 else 0

        print(f"\n--- DEPOT {d_id} ---")
        print("Demand:", d_py)
        print("Capacity:", cap)
        print("Usage:", usage_py)
        print("Vehicles routing:", veh_py)

        row[f"d{d_id}_capacity"] = cap

        # MILP (si no disponible)
        row[f"d{d_id}_demand_milp"] = None
        row[f"d{d_id}_usage_milp"] = None
        row[f"d{d_id}_veh_milp"] = None

        # PYVRP
        row[f"d{d_id}_demand_pyvrp"] = d_py
        row[f"d{d_id}_usage_pyvrp"] = usage_py
        row[f"d{d_id}_veh_pyvrp"] = veh_py

    # =========================
    # 6) GAP
    # =========================
    ub_milp = config.UB
    gap_milp = config.gap

    gap_final = (total_cost_pyvrp) - ub_milp / ub_milp if ub_milp > 0 else None

    row["ub_milp"] = ub_milp
    row["gap_milp"] = gap_milp
    row["ub_pyvrp"] = total_cost_pyvrp
    row["gap_final"] = gap_final

    print("\n========== GAP ==========")
    print("UB MILP:", ub_milp)
    print("PYVRP:", total_cost_pyvrp)
    print("FINAL GAP:", gap_final)

    return row


