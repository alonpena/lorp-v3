def adapt_instance(inst: Instance, config: ExcelConfig) -> Instance:
    """
    Adapta una instancia base (.dat) según la configuración proveniente del Excel.

    - Filtra depósitos activos
    - Ajusta capacidades si corresponde
    - Actualiza parámetros globales del modelo
    """

    # -------------------------
    # 1. FILTRAR DEPÓSITOS ACTIVOS
    # -------------------------
    active_depots = {}

    for i, depot_info in config.depots.items():
        if i in inst.depots:

            base_depot = inst.depots[i]

            active_depots[i] = {
                "x": base_depot["x"],
                "y": base_depot["y"],
                # si Excel define capacidad → usarla, sino mantener original
                "cap": depot_info["capacity"] if depot_info["capacity"] is not None else base_depot["cap"],
                "fixed_cost": 0.0 #Tenemos el costo fijo total que viene dado por el excel, pero lo dejamos así para no romper el parser
            }

    # -------------------------
    # 2. ACTUALIZAR DATA GLOBAL
    # -------------------------
    new_data = inst.data.copy()

    new_data["n_depots"] = len(active_depots)
    new_data["max_depots_open"] = len(active_depots)

    new_data["R"] = config.R
    new_data["F_R"] = config.F_R
    new_data["F_A"] = config.F_A
    new_data["Length"] = config.Length

    # -------------------------
    # 3. CREAR NUEVA INSTANCIA
    # -------------------------
    return Instance(
        depots=active_depots,
        clients=inst.clients,
        data=new_data
    )

